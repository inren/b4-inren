package de.inren.frontend.wicketstuff;

import java.io.Serializable;

/**
 * @author Ingo Renner
 * 
 */
public class EG_Person implements Serializable {
    private String name;
    private String address;
    private String age;

    public EG_Person() {
        super();
    }

    public EG_Person(String name, String address, String age) {
        this.name = name;
        this.address = address;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getAge() {
        return age;
    }

    public void setAge(String age) {
        this.age = age;
    }

    @Override
    public String toString() {
        return "EG_Person [name=" + name + ", address=" + address + ", age=" + age + "]";
    }
}